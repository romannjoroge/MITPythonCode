# Looking at a branching program
# A program makes decisions based on whether an expression is true or false

# Get input from a user
x = int(input('Enter an integer: '))

# Test whether the input is odd or even
if x % 2 == 0:
    # If true the program should print Even
    print('')
    print('Even')

# Else it should print Odd
else:
    print('')
    print('Odd')